package com.devlight.task1.task;

public class Task {
	
	String mHeader;
	String mComment;
	
	public Task(String mHeader,String mComment)
	{
		this.mHeader = mHeader;
		this.mComment = mComment;
	}
	
	
	public String getHeader()
	{
		return mHeader;
	}
	
	public String getComment()
	{
		return mComment;
	}

}
